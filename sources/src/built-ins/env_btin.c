#include "builtins.h"
#include "libft.h"

void	env_btin(char **env, int fd)
{
	int	i;

	i = 0;
	if (env == NULL)
		return ;
	while (env[i] != NULL)
	{
		ft_putstr_fd(env[i], fd);
		write(fd, "\n", 1);
		i++;
	}
}
